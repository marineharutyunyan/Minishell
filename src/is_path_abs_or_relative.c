#include "mini.h"

int	is_path_abs_or_relative(char *path)
{
	if (path[0] == '\0')
		return (0);
	if (path[0] == '/'
		|| (path[0] == '.' && path[1] == '/')
		|| (path[0] == '.' && path[1] == '.' && path[2] == '/'))
		return (1);
	return (0);
}
